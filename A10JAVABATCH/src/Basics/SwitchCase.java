package Basics;

public class SwitchCase 
{
public static void main(String[] args) 
{
 String browser="firefox";
 switch (browser) {
case("firefox"):System.out.println("firefox");
	
	break;
case("edge"):System.out.println("edge");
break;
case("chrome"):System.out.println("chrome");

default:
	break;
}
}
}
